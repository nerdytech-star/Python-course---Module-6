marks={"alice": 85, "bob": 90, "charlie": 78}
get_name=input("Enter the name of the student: ")
if get_name in marks:
    print(f"The marks of {get_name} is {marks[get_name]}")
else:
    print("Student not found")
# The code above is a simple Python script that checks if a student's name is in the dictionary 'marks' and prints their marks if found.
    



